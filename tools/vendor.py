#!/usr/bin/env python3
"""fullstack-director-vendor — vendor the framework into consumer repos (and this repo's own Claude bridge).

One command does placement + filtering + provenance — no content templating (SKILL.md is cross-harness portable;
see _artifacts/wsb-vendor-research.md). One canonical `.agents/skills/` emission serves Codex CLI + Gemini CLI
natively; a byte-identical `.claude/skills/` copy serves Claude Code. A per-file SHA-256 manifest (EOL-normalized)
makes local modification and upstream drift pure hash comparisons — the Spec-Kit/cruft pattern.

    python tools/vendor.py sync  --target <consumer-root> [--force]   # vendor/refresh a consumer
    python tools/vendor.py sync  --self                                # OPTIONAL local .claude/skills bridge —
                                                                       #   the master gitignores it (no committed
                                                                       #   bridge: skills auto-trigger and pollute
                                                                       #   framework-dev sessions; 2026-07-12)
    python tools/vendor.py check --target <consumer-root> | --self     # read-only drift detection (exit 1 on drift)
    python tools/vendor.py --self-test                                  # hermetic cases 1-9 + real-tree emission (10)

Exit codes: sync 0 ok / 2 locally-modified files skipped (list printed; --force overwrites) · check 0 clean /
1 drift (every file named). The manifest is `.director/vendor-manifest.json` at the target root. Vendored files
are never edited in a consumer — fixes flow master-first (shared/feedback-loop.md).

Stdlib only, Python 3.8+.
"""
